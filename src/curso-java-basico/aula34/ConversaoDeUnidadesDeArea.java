package loaine.cursojavabasico.aula34;

public class ConversaoDeUnidadesDeArea {

    public static double metrosParaPes(double metros){
        return metros * 10.76;
    }

    public static double pesParaCentimetros(double pes){
        return pes * 929;
    }

    public static double milhasParaAcres(double milhas){
        return milhas * 640;
    }

    public static double acreParaPes(double acre){
        return acre * 43.560;
    }
}
